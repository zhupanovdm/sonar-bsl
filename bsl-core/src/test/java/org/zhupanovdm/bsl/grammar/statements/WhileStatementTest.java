package org.zhupanovdm.bsl.grammar.statements;

import org.junit.Test;
import org.sonar.sslr.parser.LexerlessGrammar;
import org.zhupanovdm.bsl.BslGrammar;

import static org.sonar.sslr.tests.Assertions.assertThat;
import static org.zhupanovdm.bsl.BslGrammar.WHILE_STATEMENT;

public class WhileStatementTest {

    private final LexerlessGrammar g = BslGrammar.createGrammar();

    @Test
    public void test() {
        assertThat(g.rule(WHILE_STATEMENT))
                .matches("While expr1 Do EndDo")
                .matches("While expr1 Do foo(); Break; Continue EndDo");
    }

}
